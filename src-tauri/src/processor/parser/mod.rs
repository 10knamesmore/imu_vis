//! IMU 解析模块导出。
//!
//! 这里负责把“字节流”变成“可计算的物理量”。
//! 本质上做的是协议解码与量纲还原：哪些字段出现、每个字段的起止位置、
//! 小端序的 i16 值如何解释，以及对应的比例系数如何把原始值换算成物理单位。
//!
//! 大体关系可以理解为：
//! - 三轴向量：v = i16_le * scale
//! - 四元数：quat = [w, x, y, z] * scale_quat
//!
//! 解析层不做滤波和修正，只保证结构正确、数值可用。
//!
//! 目的：
//! - 将原始蓝牙字节流解码为带物理意义的 IMU 样本。
//! - 对外提供稳定的原始数据结构，供后续模块使用。
//!
//! 原理：
//! - 按协议位图解析字段，使用比例因子将整型量纲化。
//! - 例如加速度：a = raw * SCALE_ACCEL。

/// 解析实现。
pub mod logic;
/// 原始样本类型。
pub mod types;

/// 原始数据解析器。
pub use logic::ImuParser;
/// 原始样本类型与兼容别名。
pub use types::ImuSampleRaw;
